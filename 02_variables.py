
#Sure! Here's a simple explanation about variables in Python:

# Variables are used to store data in Python.
# A variable is created when you assign a value using the (=) operator.

# Integer variable
x = 10

# String variable
y = "Hello"

# Float variable
z = 3.14

# Boolean variables
w = True
m = False

# List variable
n = [1, 2, 3]

# Dictionary variable
o = {"name": "John", "age": 30}

# Tuple variable
p = (1, 2, 3)

# Set variable
q = {1, 2, 3}

# NoneType variable
r = None

# Display output
print("Integer:", x)
print("String:", y)
print("Float:", z)
print("Boolean (True):", w)
print("Boolean (False):", m)
print("List:", n)
print("Dictionary:", o)
print("Tuple:", p)
print("Set:", q)
print("NoneType:", r)
